                <h3>项目简介</h3>
                <p>
                    “保障人类健康”是我国生物安全优先考虑的战略重点之一。医学媒介生物及其相关病原体的入侵可引起如登革 热、寨卡病毒病、疟疾、锥虫病、血吸虫病、广州管圆线虫病等重要热带病的流行、疫情暴发与扩散，甚至成为 重大公共卫生安全事件，对人类健康和社会稳定构成极大威胁。
                </p>
                <p>
                    本项目拟以入侵螺类（福寿螺和褐云玛瑙螺、藁杆双脐螺）、入侵伊蚊（埃及伊蚊和白纹伊蚊）、输入性病原体 （疟原虫、锥虫和血吸虫）等为研究对象，开展它们的入侵与疾病传病相关的生物学特性研究，为建立媒介生物 及其病原体入侵、扩散和传播的快速侦查、风险评估及预警处置体系提供科学依据和技术支撑，以推动我国生物 安全科技支撑能力建设。
                </p>
                <p>
                    围绕“主要入侵生物入侵致害相关生物学特性，鉴别其分子标记以及与宿主相互作用的分子机制”的关键科学问 题，本项目拟设置4 个课题。
                </p>
                <p>
                    <strong>课题1：入侵媒介及相关病原入侵致害的相关生物学特性研究：</strong>将研究入侵生物的生态多型、形态学标定与以及 鉴定的分子标记，建立标准化的鉴定指南；揭示入侵媒介生物对我国本地病原体的易感性和输入性病原对我国本 地媒介生物的感染性，并探索输入性病原体通过蚊媒经卵传播实现本地化的可能性，最终确定这些入侵生物传播 疾病的风险性。
                </p>
                <p>
                    <strong>课题2：入侵媒介的生态适应性及进化研究研究：</strong>围绕入侵媒介生物的不同生态型群体，研究预测它们对新生境 的适应性及其扩散潜能；基于入侵生物不同生态型的系统地理模式、易感性差异、抗逆机制及其传播疾病潜能研 究，揭示入侵生物在扩散和定殖过程中的环境适应性及入侵扩散规律。
                </p>
                <p>
                    <strong>课题3：入侵媒介及相关病原的种质遗传学特征和生态适应的分子机制研究：</strong>运用比较基因组学和群体遗传学方 法，研究入侵媒介和病原体本地化过程中时空遗传变异规律、入侵、适应性和易感性的遗传学基础，揭示入侵生 物及其不同生态型群体的种系发生过程，评估入侵生物与本地种通过天然杂交与遗传渐渗而发生的基因组混合， 以及因此带来的扩散和致病风险。筛选可用于鉴别入侵媒介或输入病原体的分子标志。
                </p>
                <p>
                    <strong>课题4：入侵媒介和病原体/病原体与宿主（媒介）相互作用的分子机制研究：</strong>在遗传学研究的基础上，开展本地 病原体与入侵媒介生物、输入性病原体与本地宿主（含媒介生物）、外来病原体与相关病原体（含共生菌）共感 染与宿主、外来媒介生物与本地相近媒介生物相互作用的分子机制研究；为疫情评价/监控技术以及防治药物的开 发提供生物靶标。
                </p>
                <p>本项目的完成，将丰富我国重要热带病入侵媒介螺类（福寿螺、褐云玛瑙螺、双脐螺）和伊蚊（埃及伊蚊和白 纹伊蚊）入侵、扩散和传病相关的生物学特性，获得一批创新性成果（高水平论文、专著、专利和指南等），提 升我国入侵媒介生物领域的研究水平，提高国家生物安全科技支撑能力。
                </p>
